public struct Rect {
    public var x: Float = 0.0;
    public var y: Float = 0.0;
    public var width: Float = 0.0;
    public var height: Float = 0.0;

    public init(x: Float, y: Float, width: Float, height: Float) {
        self.x = x;
        self.y = y;
        self.width = width;
        self.height = height;
    }
}